; Example 1: Echo Program
; Reads one character from input and echoes it to output
;
; Instructions:
; 1. Type a character in the I/O panel input field
; 2. Click "Send" or press Enter
; 3. Click "Step" to execute IN instruction
; 4. Click "Step" again to execute OUT instruction
; 5. See the character appear in the output display

IN A          ; Read input character to register A
OUT A         ; Write register A to output
HALT          ; Stop execution
